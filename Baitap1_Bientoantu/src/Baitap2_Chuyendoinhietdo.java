
public class Baitap2_Chuyendoinhietdo {

	public static void main(String[] args) {
		// Khai báo biến và công thức chuyển
		double celsius = 25.0;
        double fahrenheit = (celsius * 9/5) + 32;
        // In kết quả chuyển đổi
        System.out.println(celsius + "°C = " + fahrenheit + "°F");

	}

}
